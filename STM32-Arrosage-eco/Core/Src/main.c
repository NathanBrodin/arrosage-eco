/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
    // Pourcentage de batterie	PC1
    float battery = 0;	// Max 4V

    // Pourcentage d'humidité	PA0
    float moisture = 0;

    // Pourcentage d'ensoleillement	PC0
    float sun = 0;	// Max 6.52V

    // Température de l'air en degrés Celsius 	PA1
    float temperature = 0;

    // Pourcentage du niveau d'eau	PA6
    float water = 0;

    // Relai en PA4

    // Infos de la plante
    int currentPlantId = 0;
    int moisture_min = 40;
    int moisture_max = 60;
    int temp_min_day = 13;
    int temp_max_day = 35;
    int temp_min_night = 10;
    int temp_max_night = 27;

    // Variables pour envoyer et recevoir les données
    char tx_data[128];
    char rx_data[256];

    uint32_t rawADCValue = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();
  MX_ADC_Init();
  /* USER CODE BEGIN 2 */

  LL_SYSTICK_EnableIT();

  // Enable the ADC1 port
  LL_ADC_Enable(ADC1);
  LL_ADC_SetCommonPathInternalCh(__LL_ADC_COMMON_INSTANCE(ADC1), LL_ADC_PATH_INTERNAL_TEMPSENSOR | LL_ADC_PATH_INTERNAL_VREFINT);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
    while (1) {

        // Moisture
        rawADCValue = read_channel(LL_ADC_CHANNEL_0);
        moisture = convert_moisture(rawADCValue);

        // Temperature
        rawADCValue = read_channel(LL_ADC_CHANNEL_1);
        temperature = convert_temperature(rawADCValue);

        // Water
        // rawADCValue = get_water();
        water = 32 ; // rawADCValue * 0.17;

        // Sun
        rawADCValue = read_channel(LL_ADC_CHANNEL_10);
        sun = convert_sun(rawADCValue);

        // Battery
        rawADCValue = read_channel(LL_ADC_CHANNEL_11);
        battery = convert_battery(rawADCValue);

        // Envoie des données au serveur
        sprintf(tx_data, "{\"battery\": %d,\"moisture\": %d,\"sun\": %d,\"temperature\": %d,\"water\": %d,\"currentPlantId\": %d}",
            (int)battery, (int)moisture, (int)sun, (int)temperature, (int)water, (int)currentPlantId);
        HAL_UART_Transmit( & huart1, (uint8_t * ) tx_data, strlen(tx_data), 100);


        if (isWateringNeeded(moisture, moisture_min)) {
            if (isTemperatureConditionsMet(temperature, temp_min_day, temp_max_day)) {
            	LL_GPIO_SetOutputPin(GPIOA, LL_GPIO_PIN_4);
            } else {
            	LL_GPIO_ResetOutputPin(GPIOA, LL_GPIO_PIN_4);
            }
        } else {
        	LL_GPIO_ResetOutputPin(GPIOA, LL_GPIO_PIN_4);
        }

        HAL_Delay(1000);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    }
  /* USER CODE END 3 */
}

