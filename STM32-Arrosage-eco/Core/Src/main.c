float convert_moisture(int moisture_ADC)
{
	return (moisture_ADC / 4095.0) * 100.0;
}

float convert_temperature(int temperature_ADC)
{
	float temperature_voltage = temperature_ADC * (3.3 / 4095);
	return ((temperature_voltage - 0.75) / 0.01) + 21;
}

float convert_water(int water_ADC)
{
	return (water_ADC / 4095.0) * 100.0;
}

float convert_battery(int battery_ADC) {
	return ((battery_ADC / 4095.0) * (3.3 / 2)) * 100;
}

float convert_sun(int sun_ADC) {
	return ((sun_ADC / 4095.0) * 1.5202) * 100;
}

int read_channel(int channel)
{
	LL_ADC_REG_SetSequencerChannels(ADC1, channel);
	LL_ADC_REG_StartConversion(ADC1);

	while(LL_ADC_IsActiveFlag_EOC(ADC1) != 1);

	return LL_ADC_REG_ReadConversionData12(ADC1);
}

int isWateringNeeded(int moisture_reading, int moisture_min) {
    return (moisture_reading < moisture_min);
}

int isTemperatureConditionsMet(int temperature_reading, int temp_min, int temp_max) {
    return (temperature_reading >= temp_min && temperature_reading <= temp_max);
}

